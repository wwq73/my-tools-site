// ========== 大衍筮法核心演算 ==========

/** 一变中的详细过程 */
export interface BianStepDetail {
  step: 1 | 2 | 3           // 第几变
  totalBefore: number        // 本变开始前策数
  left: number               // 左堆策数（分二）
  right: number              // 右堆策数
  guaYi: 1                   // 挂一（从右堆取一策）
  leftRemainder: number      // 左堆揲四余数
  rightRemainder: number     // 右堆揲四余数
  guiQi: number              // 归奇数（左右余数 + 挂一）
  totalAfter: number         // 本变结束后剩余策数
}

/** 三变的完整过程 */
export interface SanBianDetail {
  steps: [BianStepDetail, BianStepDetail, BianStepDetail]
  finalRemainder: number     // 三变最终剩余策数（36/32/28/24）
  value: YaoValue            // 爻值 = finalRemainder ÷ 4
}

/**
 * 大衍筮法：一变（返回过程详情）
 *
 * 步骤：
 * 1. 分二：将策数随机分成左右两堆
 * 2. 挂一：从右堆取一策，夹于左手小指间
 * 3. 揲四：左右两堆分别除以4取余数（整除则余4）
 * 4. 归奇：将左右余数和挂一的那一策合在一起，从总数中减去
 */
function yiBian(total: number, step: 1 | 2 | 3): BianStepDetail {
  // 第一步：分二
  const split = Math.floor(Math.random() * (total - 1)) + 1
  const left = split
  let right = total - split

  // 第二步：挂一（从右堆取一策）
  right -= 1
  const guaYi = 1 as const

  // 第三步：揲四（左右分别模4）
  const leftRemainder = left % 4 || 4
  const rightRemainder = right % 4 || 4

  // 第四步：归奇
  const guiQi = leftRemainder + rightRemainder + guaYi
  const totalAfter = total - guiQi

  return { step, totalBefore: total, left, right, guaYi, leftRemainder, rightRemainder, guiQi, totalAfter }
}

/**
 * 大衍筮法：三变得一爻（含详细过程）
 *
 * 大衍之数五十，其用四十有九。
 * 分而为二以象两，挂一以象三，
 * 揲之以四以象四时，归奇于扐以象闰。
 * 五岁再闰，故再扐而后挂。
 */
export function sanBianDeYiYaoDetailed(): SanBianDetail {
  const step1 = yiBian(49, 1)
  const step2 = yiBian(step1.totalAfter, 2)
  const step3 = yiBian(step2.totalAfter, 3)

  const finalRemainder = step3.totalAfter
  const value = (finalRemainder / 4) as YaoValue

  return { steps: [step1, step2, step3], finalRemainder, value }
}

/**
 * 大衍筮法：三变得一爻（仅返回值，内部用）
 *
 * @returns 爻值（6/7/8/9）
 */
export function sanBianDeYiYao(): YaoValue {
  let total = 49
  total = yiBian(total, 1).totalAfter
  total = yiBian(total, 2).totalAfter
  total = yiBian(total, 3).totalAfter
  return (total / 4) as YaoValue
}

/**
 * 生成一爻 + 三变过程
 * 供分步摇卦 UI 调用，展示策数变化
 */
export function generateYaoWithSteps(): { yao: Yao; detail: SanBianDetail } {
  const detail = sanBianDeYiYaoDetailed()

  const yao: Yao = {
    position: 0,
    type: yaoValueToType(detail.value),
    value: detail.value,
    isChanging: isBianYao(detail.value),
    symbol: yaoValueToSymbol(detail.value)
  }

  return { yao, detail }
}

// ========== 工具函数 ==========

/** 爻值转爻类型 */
function yaoValueToType(value: YaoValue): YaoType {
  switch (value) {
    case 6: return '老阴'
    case 7: return '少阳'
    case 8: return '少阴'
    case 9: return '老阳'
  }
}

/** 爻值转符号 */
function yaoValueToSymbol(value: YaoValue): '⚋' | '⚊' {
  // 阴爻（6/8）为偶数，阳爻（7/9）为奇数
  return value % 2 === 0 ? '⚋' : '⚊'
}

/** 是否为变爻 */
function isBianYao(value: YaoValue): boolean {
  return value === 6 || value === 9
}

/** 变爻取反后的符号 */
function getChangedSymbol(value: YaoValue): '⚋' | '⚊' {
  // 阳变阴，阴变阳
  return value % 2 === 1 ? '⚋' : '⚊'
}

/** 解析 summary 字段（原文||签诗） */
function parseSummary(raw: string): { classic: string; qianshi: string } {
  const parts = raw.split('||')
  return {
    classic: (parts[0] || '').trim(),
    qianshi: (parts[1] || '').trim()
  }
}

