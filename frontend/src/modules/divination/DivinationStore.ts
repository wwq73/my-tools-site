// ========== 类型定义 ==========

/** 爻值：6=老阴, 7=少阳, 8=少阴, 9=老阳 */
export type YaoValue = 6 | 7 | 8 | 9

/** 爻的类型 */
export type YaoType = '老阴' | '少阴' | '少阳' | '老阳'

/** 单爻 */
export interface Yao {
  position: number      // 0=初爻, 1=二爻, 2=三爻, 3=四爻, 4=五爻, 5=上爻
  type: YaoType
  value: YaoValue
  isChanging: boolean
  symbol: '⚋' | '⚊'
}

/** 变爻详情 */
export interface BianYaoInfo {
  yaoPosition: number   // 第几爻（1-6，1=初爻，6=上爻）
  yaoIndex: number      // 数组索引（0-5）
  from: '老阴' | '老阳'
  fromSymbol: '⚋' | '⚊'
  toSymbol: '⚊' | '⚋'
}

/** 单卦信息 */
export interface GuaInfo {
  name: string
  symbol: string
  binary: string
  fortune: string
  summary: {
    classic: string     // 周易原文（卦辞 + 大象传）
    qianshi: string     // 民间通俗签诗
  }
}

/** 完整的起卦结果 */
export interface GuaResult {
  primary: GuaInfo
  bianYaoList: BianYaoInfo[]
  changed: GuaInfo | null   // 无变爻时为 null
}

/** 占卦记录 */
export interface DivinationRecord {
  id: string
  question: string
  yaoList: Yao[]
  guaResult: GuaResult
  timestamp: number
  interpretation: string
}

