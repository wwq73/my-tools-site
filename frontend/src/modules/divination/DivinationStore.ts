/**
 * 根据6爻组装卦结果
 */
export function assembleResult(yaoList: Yao[]): GuaResult {
  // 按初爻到上爻排列（position 0=初爻, 5=上爻）
  const sorted = [...yaoList].sort((a, b) => a.position - b.position)

  // 生成本卦二进制键（初爻在索引0，上爻在索引5）
  let primaryBinary = ''
  let changedBinary = ''
  const bianYaoList: BianYaoInfo[] = []

  for (let i = 0; i < 6; i++) {
    const yao = sorted[i]

    // 本卦键
    primaryBinary += yao.value % 2 === 1 ? '1' : '0'

    // 变卦键：变爻取反，不变爻保持
    if (yao.isChanging) {
      changedBinary += yao.value % 2 === 1 ? '0' : '1'
      bianYaoList.push({
        yaoPosition: i + 1,  // 初爻=1, 二爻=2, ..., 上爻=6
        yaoIndex: i,
        from: yao.type as '老阴' | '老阳',
        fromSymbol: yao.symbol,
        toSymbol: getChangedSymbol(yao.value)
      })
    } else {
      changedBinary += yao.value % 2 === 1 ? '1' : '0'
    }
  }

  // 查找本卦
  const primaryGua = GUA_64[primaryBinary]
  if (!primaryGua) {
    throw new Error(`未找到本卦: ${primaryBinary}`)
  }

  // 查找变卦（如果无变爻，返回 null）
  const hasChange = changedBinary !== primaryBinary
  const changedGua = hasChange ? GUA_64[changedBinary] : null
  if (hasChange && !changedGua) {
    throw new Error(`未找到变卦: ${changedBinary}`)
  }

  return {
    primary: {
      name: primaryGua.name,
      symbol: primaryGua.symbol,
      binary: primaryBinary,
      fortune: primaryGua.fortune,
      summary: parseSummary(primaryGua.summary)
    },
    bianYaoList,
    changed: changedGua ? {
      name: changedGua.name,
      symbol: changedGua.symbol,
      binary: changedBinary,
      fortune: changedGua.fortune,
      summary: parseSummary(changedGua.summary)
    } : null
  }
}

/**
 * 大衍筮法一键起卦（完整版）
 *
 * 十八变而成卦：
 * - 每爻三变，共六爻，合计十八变
 * - 初爻最先成，上爻最后成
 * - 遇老阴(6)或老阳(9)为变爻，少阴(8)少阳(7)不变
 *
 * @returns 包含本卦、变爻信息、变卦的完整结果
 */
export function qiGua(): GuaResult {
  const yaoList: Yao[] = []

  // 从初爻到上爻，逐爻生成（共6爻 × 3变 = 18变）
  for (let i = 0; i < 6; i++) {
    const value = sanBianDeYiYao()

    yaoList.push({
      position: i,  // 0=初爻, 1=二爻, ..., 5=上爻
      type: yaoValueToType(value),
      value,
      isChanging: isBianYao(value),
      symbol: yaoValueToSymbol(value)
    })
  }

  return assembleResult(yaoList)
}

