/**
 * 生成占卦解析文本
 */
export function generateDivinationInterpretation(question: string, result: GuaResult): string {
  const { primary, bianYaoList, changed } = result

  let interpretation = `【占卦问题】\n${question}\n\n`

  // 本卦
  interpretation += `【本卦】${primary.name}卦 ${primary.symbol}\n`
  interpretation += `📖 《周易》原文\n${primary.summary.classic}\n\n`
  interpretation += `🏮 签诗\n${primary.summary.qianshi}\n\n`

  // 变卦
  if (changed) {
    interpretation += `【变卦】${changed.name}卦 ${changed.symbol}\n`
    interpretation += `📖 《周易》原文\n${changed.summary.classic}\n\n`
    interpretation += `🏮 签诗\n${changed.summary.qianshi}\n\n`
  }

  // 变爻分析（含爻辞）
  if (bianYaoList.length > 0) {
    const primaryBinaryRow = primary.binary
    const primaryYaoCi = GUA_YAO_CI[primaryBinaryRow]

    interpretation += `【变爻分析】\n`
    const positionNames = ['初', '二', '三', '四', '五', '上']
    for (const b of bianYaoList) {
      const yaoName = getYaoName(primaryBinaryRow, b.yaoIndex)
      const yaoCi = primaryYaoCi?.[b.yaoIndex]
      interpretation += `\n▸ ${positionNames[b.yaoIndex]}爻（${yaoName}）：${b.from} ${b.fromSymbol} → ${b.toSymbol}\n`
      if (yaoCi) {
        interpretation += `  爻辞：${yaoCi.c}\n`
        interpretation += `  象曰：${yaoCi.x}\n`
        interpretation += `  白话：${yaoCi.b}\n`
      }
    }
    interpretation += `\n`
  }

  // 综合判断
  interpretation += `【综合判断】\n`
  const n = bianYaoList.length
  if (n === 0) {
    interpretation += `六爻皆静（静卦），以本卦卦辞为主。此事现状稳定，可按当前方向继续。\n`
  } else if (n === 1) {
    interpretation += `一爻变，以变爻爻辞为主。此事关键在于一点，需重点关注上述变爻的指引。\n`
  } else if (n === 2) {
    interpretation += `二爻变，以两变爻爻辞综合判断，以上爻为主。此事有两方面变化，需权衡利弊。\n`
  } else if (n === 3) {
    interpretation += `三爻变，以本卦卦辞和变卦卦辞综合判断。此事变化较大，需灵活应对。\n`
  } else if (n === 4) {
    interpretation += `四爻变，以变卦中不变的两爻判断，以下爻为主。此事根基动摇，需稳固基础。\n`
  } else if (n === 5) {
    interpretation += `五爻变，以变卦中不变的一爻判断。此事大势已去，需顺应变化。\n`
  } else if (n === 6) {
    interpretation += `六爻皆变，以变卦卦辞判断，乾坤两卦用"用九""用六"。此事彻底转变，需全新开始。\n`
  }

  interpretation += `\n【建议】\n`
  interpretation += `心诚则灵，占卦结果仅供参考。真正的命运掌握在自己手中，行动才是改变的关键。`

  return interpretation
}

// ========== Zustand Store ==========

interface DivinationState {
  records: DivinationRecord[]
  isLoading: boolean
  currentStep: number        // 当前摇卦步骤 0-6
  currentYao: Yao[]
  currentDetail: SanBianDetail | null  // 当前步的三变详情
  currentQuestion: string
  currentResult: DivinationRecord | null

  addRecord: (record: DivinationRecord) => void
  setLoading: (loading: boolean) => void
  setCurrentStep: (step: number) => void
  addYao: (yao: Yao) => void
  setCurrentDetail: (detail: SanBianDetail | null) => void
  resetYao: () => void
  setCurrentQuestion: (question: string) => void
  setCurrentResult: (result: DivinationRecord | null) => void
  clearRecords: () => void
}

export const useDivinationStore = create<DivinationState>()(
  persist(
    (set) => ({
      records: [],
      isLoading: false,
      currentStep: 0,
      currentYao: [],
      currentDetail: null,
      currentQuestion: '',
      currentResult: null,

      addRecord: (record) => {
        set((state) => ({
          records: [record, ...state.records].slice(0, 30)
        }))
      },

      setLoading: (loading) => set({ isLoading: loading }),
      setCurrentStep: (step) => set({ currentStep: step }),
      addYao: (yao) => {
        set((state) => ({
          currentYao: [...state.currentYao, yao]
        }))
      },
      setCurrentDetail: (detail) => set({ currentDetail: detail }),
      resetYao: () => set({ currentYao: [], currentStep: 0, currentDetail: null }),
      setCurrentQuestion: (question) => set({ currentQuestion: question }),
      setCurrentResult: (result) => set({ currentResult: result }),
      clearRecords: () => set({ records: [] })
    }),
    {
      name: 'divination-storage'
    }
  )
)
